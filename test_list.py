mylist=["mango","banana","grapes"]
print(mylist) #printing the list
print(mylist[0]) #accessing the first element of the list
print(mylist[1]) #accessing the second element of the list
print(mylist[2]) #accessing the third element of the list   
mylist[2] = "orange" #modifying the third element of the list
print(mylist) #printing the modified list
mylist.append("kiwi") #adding a new element to the end of the list
print(mylist) #printing the modified list
mylist.insert(1, "grape") #inserting a new element at index 1
print(mylist) #printing the modified list
mylist.remove("banana") #removing an element from the list`
print(mylist) #printing the modified list
mylist.pop(1) #removing the last element from the list
print(mylist) #printing the modified list